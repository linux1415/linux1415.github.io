namespace Practice
{
    class Math
    {
        public static int Add(int a, int b)
        {
            return a + b;
        }

        public static int subtract(int a, int b)
        {
            return a - b;
        }

        public static double moneyEarned(int hoursWorked, double hourlyPay)
        {
            return hoursWorked * hourlyPay;
        }

    }
}
